        int telefono = Integer.parseInt(jTextField7.getText());
        String provincia = jTextField2.getText();
        String ciudad = jTextField3.getText();
        String calle = jTextField5.getText();
        int codigopostal = Integer.parseInt(jTextField4.getText());
        int numero = Integer.parseInt(jTextField6.getText());
        
